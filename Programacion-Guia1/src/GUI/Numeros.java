    private void PromedioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PromedioActionPerformed
        String n1,n2,n3;
        float num1,num2,num3,c1;
        float prom;
        
        if (Numero1.getText().isEmpty()==true && Numero2.getText().isEmpty()==true && Numero3.getText().isEmpty()==true)    JOptionPane.showMessageDialog(rootPane,"Ingrese valores en al menos un campo");
        
        else
        {
            c1=3;
            if (Numero1.getText().isEmpty()==true) 
            {
                Numero1.setText("0");
            }
        
            if (Numero2.getText().isEmpty()==true) 
            {
                Numero2.setText("0");
            }
        
            if (Numero3.getText().isEmpty()==true) 
            {
                Numero3.setText("0");
            }
        
            n1=Numero1.getText();
            n2=Numero2.getText();
            n3=Numero3.getText();
        
            num1=Float.parseFloat(n1);
            num2=Float.parseFloat(n2);
            num3=Float.parseFloat(n3);

            if (num1==0)    c1--;
            if (num2==0)    c1--;
            if (num3==0)    c1--;
            
            prom=(num1+num2+num3)/c1;
            
            JOptionPane.showMessageDialog(rootPane, "El promedio de los números es "+prom);
        }            
    }//GEN-LAST:event_PromedioActionPerformed

