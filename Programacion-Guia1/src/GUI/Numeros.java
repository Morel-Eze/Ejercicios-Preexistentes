    private void MenorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenorActionPerformed
        
        String n1,n2,n3;
        float num1,num2,num3;
        
        if (Numero1.getText().isEmpty()==true && Numero2.getText().isEmpty()==true && Numero3.getText().isEmpty()==true)    JOptionPane.showMessageDialog(rootPane, "Ingrese valores en al menos un campo");
        
        else
        {
            if (Numero1.getText().isEmpty()==true)  Numero1.setText("0");
            if (Numero2.getText().isEmpty()==true)  Numero2.setText("0");
            if (Numero3.getText().isEmpty()==true)  Numero3.setText("0");
                
            n1=Numero1.getText();
            n2=Numero2.getText();
            n3=Numero3.getText();
        
            num1=Float.parseFloat(n1);
            num2=Float.parseFloat(n2);
            num3=Float.parseFloat(n3);
        
            if (num1==0)    num1=999999999.99999999999999999f;
            if (num2==0)    num2=999999999.99999999999999999f;
            if (num3==0)    num3=999999999.99999999999999999f;
        
            if (num1<=num2 && num1<=num3)   JOptionPane.showMessageDialog(rootPane, "El menor número ingresado es: "+num1);
            if (num2<=num1 && num2<=num3)   JOptionPane.showMessageDialog(rootPane, "El menor número ingresado es: "+num2);
            if (num3<=num2 && num3<=num1)   JOptionPane.showMessageDialog(rootPane, "El menor número ingresado es: "+num3);          
        }
    }//GEN-LAST:event_MenorActionPerformed

